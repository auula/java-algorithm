package me.ibyte.algorithm.sort;


import me.ibyte.algorithm.Sort;

public class BubbleSort implements Sort {
    @Override
    public Integer[] sort(Integer[] arr) {
        int temp;
        for (int i = 0; i < arr.length; i++) {
            for (int j = 0; j < arr.length - 1 - i; j++) {
                if (arr[j] > arr[j+1]){
                    temp = arr[j+1];
                    arr[j+1] = arr[j];
                    arr[j] = temp;
                }
            }
        }
        return arr;
    }
}
